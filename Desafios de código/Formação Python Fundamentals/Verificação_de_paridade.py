#Neste desafio, você deve escreva uma solução que receba um número inteiro como entrada e determine se ele é par ou ímpar. 
# Dessa forma, a solução deve retornar uma string indicando Par se o número for par e Ímpar se o número for ímpar.

num=int(input())
