#Faça um programa que leia o valor em metros e o exiba convertido em centímetros e milímetros
valor = float(input('Digite o valor que deseja converter \n'))
centímetros = valor*100
milímetros = valor*1000
print(f'O valor convertido para centímetros é {centímetros}')
print(f'O valor convertido para milímetros é {milímetros}')
